score = float(input('Enter your Score : '))
if score >= 80:
    print('You Grade : A')
elif score >= 75:
    print('You Grade : B+')
elif score >= 70:
    print('You Grade : B')
elif score >= 65:
    print('You Grade : C+')
elif score >= 60:
    print('You Grade : C')
elif score >= 55:
    print('You Grade : D+')
elif score >= 50:
    print('You Grade : D')
else:
    print('You Grade : F')
